# 4. 두 개의 숫자를 입력받아, 
# 첫 번째 수에서 두 번째 수를 뺀 결과를 출력하는 함수를 작성하고 호출하세요
# 단, 첫 번째 수가 두 번째 수보다 작으면 "계산할 수 없습니다"를 출력합니다

# 입력값 예시: 20, 10
# 출력 예시: "20 - 10 = 10"

# 입력값 예시: 10, 20
# 출력 예시: "계산할 수 없습니다"

def sub(n1, n2):
    if n1 < n2:
        print("계산할 수 없습니다")
    else:    
        result = n1 - n2
        print(f"{n1} - {n2} ={result}")

sub(20, 10)

# 5. 아래와 같이 숫자를 입력 받아 양수/음수/0을 판별하는 함수를 작성하고 호출하세요
# 입력값 예시: 10
# 출력 예시: "양수입니다"
# 입력값 예시: -5
# 출력 예시: "음수입니다"
# 입력값 예시: 0
# 출력 예시: "0입니다"
def check_num(n):
    if n > 0:
        print("양수입니다")
    elif n < 0:
        print("음수입니다")
    else:
        print("0입니다")

check_num(10)

