# 7. 아래와 같이 숫자를 입력받아 양수이면 True, 아니면 False를 반환하는 함수를 작성하고 호출하세요
# 입력값 예시: 10
# 반환값 예시: True
# 입력값 예시: -3
# 반환값 예시: False

def is_positive(n):
    if n > 0:
        return True
    else:
        return False

result = is_positive(10)
print(result)
